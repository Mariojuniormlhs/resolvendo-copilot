#RECEBEREMOS DO USUÁRIO 3 NOTAS OU VALORES E PRECISAREMOS TIRAR A MEDIA

#RECEBE OS VALORES DO USER
N1 = float(input("Digite a nota 1: "))
N2 = float(input("Digite a nota 2: "))
N3 = float(input("Digite a nota 3: "))

#REALIZA O CALCULO PARA OBTER A MEDIA

media = (N1 + N2 + N3)/3

print("Sua média é:",media)
